package com.example.kalkulator;

public class Calculator {

    public int addTwoInts(int first, int second) {
        int sumFirstAndSecond = first + second;
        return sumFirstAndSecond;
    }

    public int substractTwoInts(int first, int second) {
        int resultOfSubstractFirstAndSecond = first - second;
        return resultOfSubstractFirstAndSecond;
    }

}
